const mongoose = require("mongoose"),
    { Schema } = mongoose;

const userSchema = new Schema({
    username: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    },
    admin: Boolean
});

module.exports = mongoose.model("User", userSchema);
